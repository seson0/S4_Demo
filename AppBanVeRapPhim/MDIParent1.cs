        private void LoadFormCon<T>() where T : Form, new()
        {
            Form form = Application.OpenForms.Cast<Form>().FirstOrDefault(f => f is T);

            if (form == null)
            {
                form = new T();
                form.MdiParent = this;
                form.Show();
            }
            else
            {
                form.Activate();
            }
        }

